    private void btninsertarActionPerformed(java.awt.event.ActionEvent evt) {                                            
        // TODO add your handling code here:
        conectar cc = new conectar();
        Connection cn = cc.conexion();
        String cod,nom,ape;
        String sql= "";
        cod = txtcodigo.getText();
        nom = txtnombre.getText();
        ape = txtapellido.getText();
        sql = "insert into tblalumnos (codigoalu,nombre,apellidos) values (?,?,?)";
         try {
             PreparedStatement pst = cn.prepareStatement(sql);
             pst.setString(1,cod);
             pst.setString(2,nom);
             pst.setString(3,ape);
             int registro=pst.executeUpdate();
             if (registro>0)
             {
                 JOptionPane.showMessageDialog(null, "Registro almacenado correctamente");
                 limpiar();
             }
         } catch (Exception e) {
             JOptionPane.showMessageDialog(null, "Registro NO almacenado " + e.getMessage());

    }                                           
}
    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {                                              
        // TODO add your handling code here:
         conectar cc = new conectar();
        Connection cn = cc.conexion();
        String cod,nom,ape;
        String sql ="";
        cod = txtcodigo.getText();
        nom = txtnombre.getText();
        ape = txtapellido.getText();
        sql = "update tblalumnos SET "
            + "nombre= '"+nom+"', "
            + "apellidos= '"+ape+"' "
            + "WHERE codigoalu= '"+cod+"'";
        System.out.println(sql);
        try {
            PreparedStatement pst = cn.prepareStatement(sql);
            
            int registro=pst.executeUpdate();
            if (registro>0)
            {
                JOptionPane.showMessageDialog(null,"Registro actualizado correctamente");
                limpiar();
            }
            else {
                JOptionPane.showMessageDialog(null, "Registro no encontrado para actualizar");
                limpiar();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Registro NO actualizado" + e.getMessage());
        }
                                             

    }                                             

